int mbedtls_test_rnd_std_rand(void *rng_state,
								unsigned char *output,
								size_t len)
{
#if !defined(__OpenBSD__) && !defined(__NetBSD__)
	size_t i;

	if (rng_state != NULL) {
		rng_state  = NULL;
	}

	for (i = 0; i < len; ++i) {
		sys_rand_get(&output[i], 1);
	}
#else
	if (rng_state != NULL) {
		rng_state = NULL;
	}

	arc4random_buf(output, len);
#endif /* !OpenBSD && !NetBSD */

	return 0;
}

void dump_key(char *text, unsigned char *key, size_t key_len)
{
	int i;

	printf("%s\n", text);
	for (i = 0; i < key_len; i++)
	{
		printf("0x%02x, ", key[i]);

		if (i%16 == 15)
			printf("\n");
	}
	printf("\n");
}

